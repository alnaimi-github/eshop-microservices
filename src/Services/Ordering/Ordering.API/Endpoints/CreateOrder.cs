using Ordering.Application.Orders.Commands.CreateOrder;

namespace Ordering.API.Endpoints;

public sealed record CreateOrderRequest(OrderDto Order);
public sealed record CreateOrderResponse(Guid Id);
public sealed class CreateOrder : ICarterModule
{
    public void AddRoutes(IEndpointRouteBuilder app)
    {
        app.MapPost("/orders", async (CreateOrderRequest request, ISender sender) =>
        {
            var command = request.Adapt<CreateOrderCommand>();

            var result = await sender.Send(command);

            var response = result.Adapt<CreateOrderResponse>();

            return Results.CreatedAtRoute("/orders/", new { id = response.Id }, response);
        })
        .WithName("CreateOrder")
        .Produces<CreateOrderResponse>(StatusCodes.Status201Created)
        .ProducesProblem(StatusCodes.Status400BadRequest)
        .WithSummary("Create Order")
        .WithDescription("Create Order");
    }
}